// Map IFD1 and Thumbnail to IFD0 tags as they share the TIFF tag set
tagMaps.ifd1 = tagMaps.ifd0;
tagMaps.thumbnail = tagMaps.ifd0;

export const getTagHex = (group: string, tag: string): number | undefined => {
    const groupKey = group.toLowerCase();

    // Direct lookup in specific group
    if (tagMaps[groupKey] && tagMaps[groupKey][tag] !== undefined) {
        return tagMaps[groupKey][tag];
    }

    // Checking ifd0/exif overlap for some common tags
    if (tagMaps.ifd0 && tagMaps.ifd0[tag] !== undefined) return tagMaps.ifd0[tag];
    if (tagMaps.exif && tagMaps.exif[tag] !== undefined) return tagMaps.exif[tag];
    if (tagMaps.general && tagMaps.general[tag] !== undefined) return tagMaps.general[tag];

    return undefined;
};
